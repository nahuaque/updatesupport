"""Conformal classification prediction-set stability example.

The example audits whether aggregate conformal prediction-set quantities are
stable to hidden subgroup recomposition. It is synthetic and dependency-free:
class probabilities are generated by a small deterministic scoring rule, a
split-conformal score threshold is computed from calibration rows, and
``updatesupport`` audits prediction-set size, ambiguous-set rate, coverage,
miscoverage, and risky-label containment.

Run from the repository root with:

    uv run python examples/conformal_classification_stability.py

Optionally write the Markdown report:

    uv run python examples/conformal_classification_stability.py \
        --output data/conformal_classification_stability.md
"""

