def fit_conformal_threshold(
    *,
    alpha: float = ALPHA,
    threshold_buffer: float = CONFORMAL_THRESHOLD_BUFFER,
) -> float:
    """Return the split-conformal class-probability inclusion threshold."""

    calibration_rows = _synthetic_calibration_rows()
    nonconformity_scores = []
    for row in calibration_rows:
        probabilities = _class_probabilities(row)
        true_probability = probabilities[str(row[OUTCOME_COLUMN])]
        nonconformity_scores.append(1.0 - true_probability)
    q_hat = _conformal_quantile(nonconformity_scores, alpha=alpha)
    return max(0.0, min(1.0, 1.0 - q_hat - float(threshold_buffer)))


def build_conformal_result(
    rows: Sequence[Mapping[str, Any]] | None = None,
    *,
    alpha: float = ALPHA,
) -> us.ConformalAdapterResult:
    """Attach conformal prediction sets to retained classification cells."""

    rows = synthetic_classification_rows() if rows is None else rows
    threshold = fit_conformal_threshold(alpha=alpha)
    prediction_sets: list[tuple[str, ...]] = []
    predictions: list[str] = []
    y_true: list[str] = []
    for row in rows:
        probabilities = _class_probabilities(row)
        prediction = max(CLASSES, key=lambda label: probabilities[label])
        prediction_set = tuple(
            label for label in CLASSES if probabilities[label] >= threshold
        )
        if not prediction_set:
            prediction_set = (prediction,)
        predictions.append(prediction)
        prediction_sets.append(prediction_set)
        y_true.append(str(row[OUTCOME_COLUMN]))

    result = us.adapt_conformal_classification(
        rows,
        prediction=predictions,
        prediction_sets=prediction_sets,
        y_true=y_true,
        positive_label=POSITIVE_LABEL,
        source="split_conformal_classification",
    )
    metadata = dict(result.metadata)
    metadata["class_probability_threshold"] = threshold
    return us.ConformalAdapterResult(
        rows=result.rows,
        source=result.source,
        source_rows=result.source_rows,
        prediction_column=result.prediction_column,
        covered_column=result.covered_column,
        miscovered_column=result.miscovered_column,
        prediction_set_size_column=result.prediction_set_size_column,
        ambiguous_set_column=result.ambiguous_set_column,
        metadata=metadata,
    )


def build_stability_report(
    result: us.ConformalAdapterResult | None = None,
    *,
    include_attribution: bool = False,
) -> us.ConformalReportingStabilityReport:
    """Build the conformal classification reporting-stability report."""

    result = build_conformal_result() if result is None else result
    return result.reporting_stability(
        public=PUBLIC_COLUMNS,
        hidden=HIDDEN_COLUMNS,
        weight=WEIGHT_COLUMN,
        candidate_refinements=CANDIDATE_REFINEMENTS,
        ambiguity_limits={
            "prediction_set_size": 0.30,
            "ambiguous_set": 0.20,
            "covered": 0.20,
            "miscovered": 0.20,
            "contains_positive_label": 0.25,
        },
        search="beam",
        beam_width=8,
        max_added_columns=2,
        max_evaluations=64,
        exact_required=False,
        include_attribution=include_attribution,
        title="Conformal Classification Reporting Stability Example",
    )


