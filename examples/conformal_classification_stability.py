def _synthetic_calibration_rows() -> list[dict[str, Any]]:
    rows: list[dict[str, Any]] = []
    for product_area in _PRODUCT_AREAS:
        for region in _REGIONS:
            for customer_tier in _TIERS:
                for acquisition_channel in _CHANNELS:
                    for tenure_band in _TENURES:
                        for device_type in _DEVICES:
                            for language in _LANGUAGES:
                                row = {
                                    "product_area": product_area,
                                    "region": region,
                                    "customer_tier": customer_tier,
                                    "acquisition_channel": acquisition_channel,
                                    "tenure_band": tenure_band,
                                    "device_type": device_type,
                                    "language": language,
                                }
                                row[OUTCOME_COLUMN] = _observed_label(row)
                                rows.append(row)
    return rows


def _class_probabilities(row: Mapping[str, Any]) -> dict[str, float]:
    logits = {
        "approve": 1.25,
        "review": 0.45,
        "reject": 0.10,
    }
    if row["customer_tier"] == "enterprise":
        logits["approve"] += 0.35
        logits["reject"] -= 0.15
    elif row["customer_tier"] == "smb":
        logits["approve"] -= 0.28
        logits["reject"] += 0.28

    if row["product_area"] == "support":
        logits["review"] += 0.42
        logits["reject"] += 0.22
    elif row["product_area"] == "checkout":
        logits["reject"] += 0.18

    if row["region"] == "europe":
        logits["review"] += 0.12
    elif row["region"] == "latam":
        logits["review"] += 0.15
        logits["reject"] += 0.30

    if row["acquisition_channel"] == "sales_led":
        logits["approve"] += 0.22
    elif row["acquisition_channel"] == "partner":
        logits["review"] += 0.24
    elif row["acquisition_channel"] == "self_serve":
        logits["review"] += 0.24
        logits["reject"] += 0.34
    elif row["acquisition_channel"] == "affiliate":
        logits["reject"] += 0.42

    if row["tenure_band"] == "new":
        logits["review"] += 0.22
        logits["reject"] += 0.22
    else:
        logits["approve"] += 0.18

    if row["device_type"] == "mobile":
        logits["review"] += 0.14
        logits["reject"] += 0.16
    if row["language"] == "localized":
        logits["review"] += 0.20
        logits["reject"] += 0.18

    if row["acquisition_channel"] == "self_serve" and row["tenure_band"] == "new":
        logits["reject"] += 0.20
    if row["region"] == "latam" and row["language"] == "localized":
        logits["reject"] += 0.14

    return _softmax(logits)


def _observed_label(row: Mapping[str, Any]) -> str:
    probabilities = _class_probabilities(row)
    if probabilities["reject"] >= 0.31:
        return "reject"
    if probabilities["review"] >= 0.33:
        return "review"
    key = "|".join(str(row[column]) for column in HIDDEN_COLUMNS)
    raw = sum((index + 1) * ord(char) for index, char in enumerate(key)) % 17
    if raw in {0, 1} and probabilities["review"] >= 0.26:
        return "review"
    return "approve"


def _softmax(logits: Mapping[str, float]) -> dict[str, float]:
    max_logit = max(logits.values())
    exp_values = {label: math.exp(value - max_logit) for label, value in logits.items()}
    denominator = sum(exp_values.values())
    return {label: value / denominator for label, value in exp_values.items()}


def _conformal_quantile(scores: Sequence[float], *, alpha: float) -> float:
    if not 0.0 < alpha < 1.0:
        raise ValueError("alpha must be between 0 and 1")
    if not scores:
        raise ValueError("scores must be non-empty")
    sorted_scores = sorted(float(score) for score in scores)
    rank = math.ceil((len(sorted_scores) + 1) * (1.0 - alpha))
    index = min(max(rank - 1, 0), len(sorted_scores) - 1)
    return sorted_scores[index]


def _weighted_mean(
    rows: Sequence[Mapping[str, Any]],
    target: str,
    weight: str,
) -> float:
    numerator = sum(float(row[target]) * float(row[weight]) for row in rows)
    denominator = sum(float(row[weight]) for row in rows)
    return numerator / denominator


def _without_title(markdown: str) -> list[str]:
    lines = markdown.splitlines()
    if lines and lines[0].startswith("# "):
        lines = lines[1:]
        if lines and not lines[0]:
            lines = lines[1:]
    return lines


def _percent(value: float) -> str:
    return f"{100.0 * value:.2f}%"


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--output",
        type=Path,
        help="Optional Markdown output path.",
    )
    parser.add_argument(
        "--include-attribution",
        action="store_true",
        help="Include Shapley-style refinement attribution in the audit.",
    )
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    markdown = render_report(include_attribution=args.include_attribution)
    if args.output is None:
        print(markdown)
        return
    args.output.parent.mkdir(parents=True, exist_ok=True)
    args.output.write_text(markdown + "\n", encoding="utf-8")
    print(f"Wrote {args.output}")


if __name__ == "__main__":
    main()
